as begin
insert into FUNCIONARIO values
	(
	@Codigo_Funcionario,
	@Rut_Funcionario,
	@Nombre_Funcionario,
	@Sexo_Funcionairo,
	@Fecha_Nacimiento_Funcionario,
	@Celular_Funcionario,
	@Correo_Funcionario,
	@Cargo_Funcionario,
	@Profesion_Funcionario,
	@Departamento_Funcionario,
	@Ciclo_Funcionario,
	@Horario_Asignado_Funcionario
	)
end 

go
create proc Registrar_Registro
(
	@Rut_Fun	varchar	(10),
	@Nom_Fun	varchar	(100),
	@Cod_Mod	int,
	@Tipo_Reg	varchar (10)
)
as begin
insert into registro values
	(
		@Rut_Fun,
		@Nom_Fun,
		@Cod_Mod,
		@Tipo_Reg,
		GETDATE(),
		GETDATE(),
		DATEADD(second,20,GETDATE())
	)
end 










